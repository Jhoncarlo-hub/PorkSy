create extension if not exists pgcrypto;

do $$ begin create type app_role as enum ('admin','staff','vet'); exception when duplicate_object then null; end $$;
do $$ begin create type record_status as enum ('active','inactive','sold','deceased','transferred'); exception when duplicate_object then null; end $$;
do $$ begin create type pen_status as enum ('active','maintenance','inactive'); exception when duplicate_object then null; end $$;
do $$ begin create type task_status as enum ('pending','completed','overdue','cancelled'); exception when duplicate_object then null; end $$;
do $$ begin create type payment_status as enum ('paid','partial','pending'); exception when duplicate_object then null; end $$;

create table if not exists profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  username text not null unique,
  full_name text not null,
  role app_role not null default 'staff',
  email text,
  contact text,
  status text not null default 'active',
  created_at timestamptz not null default now()
);

create table if not exists pig_breeds (
  id bigint generated by default as identity primary key,
  breed_name text not null,
  description text,
  avg_gestation_days integer default 114,
  avg_litter_size integer default 10,
  created_at timestamptz not null default now()
);

create table if not exists pens (
  id bigint generated by default as identity primary key,
  pen_number text not null unique,
  pen_type text not null,
  capacity integer not null default 10,
  current_count integer default 0,
  description text,
  status pen_status default 'active',
  created_at timestamptz not null default now()
);

create table if not exists pigs (
  id bigint generated by default as identity primary key,
  pig_tag text not null unique,
  breed_id bigint references pig_breeds(id) on delete set null,
  gender text not null check (gender in ('male','female')),
  pig_type text not null check (pig_type in ('boar','sow','gilt','grower','finisher','piglet')),
  pen_id bigint references pens(id) on delete set null,
  date_of_birth date,
  source text default 'born_on_farm',
  purchase_price numeric(10,2) default 0,
  mother_id bigint references pigs(id) on delete set null,
  father_id bigint references pigs(id) on delete set null,
  birth_weight numeric(6,2),
  current_weight numeric(6,2),
  status record_status default 'active',
  notes text,
  created_by uuid references profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists feed_types (
  id bigint generated by default as identity primary key,
  feed_name text not null,
  feed_category text not null,
  brand text,
  protein_percent numeric(5,2),
  price_per_kg numeric(8,2),
  unit text default 'kg',
  description text,
  status text default 'active',
  created_at timestamptz not null default now()
);

create table if not exists feed_inventory (
  id bigint generated by default as identity primary key,
  feed_type_id bigint not null references feed_types(id) on delete cascade,
  quantity_kg numeric(10,2) not null default 0,
  minimum_stock_kg numeric(10,2) default 50,
