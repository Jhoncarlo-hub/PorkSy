create table if not exists feeding_records (
  id bigint generated by default as identity primary key,
  pen_id bigint references pens(id) on delete set null,
  pig_id bigint references pigs(id) on delete set null,
  feed_type_id bigint not null references feed_types(id) on delete cascade,
  feeding_date date not null,
  feeding_time text default 'morning',
  quantity_kg numeric(8,2) not null,
  notes text,
  recorded_by uuid references profiles(id) on delete set null,
