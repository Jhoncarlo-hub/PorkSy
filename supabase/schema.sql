create or replace function touch_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at = now();
  return new;
end $$;

drop trigger if exists pigs_touch_updated_at on pigs;
create trigger pigs_touch_updated_at before update on pigs for each row execute function touch_updated_at();

create or replace function refresh_pen_counts()
returns trigger language plpgsql as $$
begin
  update pens set current_count = (
    select count(*) from pigs where pigs.pen_id = pens.id and pigs.status = 'active'
  );
  return null;
end $$;

drop trigger if exists pigs_refresh_pen_counts on pigs;
create trigger pigs_refresh_pen_counts after insert or update or delete on pigs for each statement execute function refresh_pen_counts();

create or replace function current_role()
returns app_role language sql stable security definer as $$
  select role from profiles where id = auth.uid()
$$;

create or replace function can_read_module(module text)
returns boolean language plpgsql stable security definer as $$
declare r app_role;
begin
  select current_role() into r;
  if r = 'admin' then return true; end if;
  if module in ('tasks','weight_records') and r in ('staff','vet') then return true; end if;
  if module in ('pens','feeding_records','feed_inventory') and r = 'staff' then return true; end if;
  if module in ('pigs','health_records','breeding_records','reports') and r = 'vet' then return true; end if;
  return false;
end $$;

